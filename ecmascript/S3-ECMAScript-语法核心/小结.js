/**
 * JavaScript 的语法核心特性在 ECMA-262 中以伪语言 ECMAScript 的形式来定义。
 *  ECMAScript 包含所有基本语法、操作符、数据类型和对象，能完成基本的计算任务，
 *  但没有提供输入和产生输出的机制。理解 ECMAScript 及其复杂的细节是完全理解浏览器中 JavaScript 的关键。
 */

/**
 * 总结：
 * 1. ECMAScript 的基本类型：Undefined、Null、Number、Boolean、String、Symbol
 * 2. 与其他语言不同，ECMAScript 不区分整数与浮点数，只有 Number 这一种数值类型。
 * 3. Object 是一种复杂数据类型，它是这门语言所有对象的基类。
 * 4. 严格模式为这门语言中某些容易出错的部分施加了限制。
 * 5. ECMAScript 提供了 C 语言和类 C 语言中常见的很多基本操作符，包括数学操作符、布尔操作符、关系操作符、相等操作符、赋值操作符等
 * 6. 这门语言中的流控制语句大多是葱其他语言借鉴而来的，比如 if 语句、for 语句 和 switch 语句等。
 * 
 * 
 * ECMAScript 中的函数与其他语言中的函数不一样。
 * 1. 不需要指定函数的返回值，因为任何函数可以在任何时候返回任何值。
 * 2. 不指定返回值的函数实际上会返回特殊值 undefined
 */